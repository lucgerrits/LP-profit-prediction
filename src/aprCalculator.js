// Function to calculate the APR for a pool
export function calculateAPR(feeTier, volumeUSD, liquidity)  {
    // Validate inputs to avoid invalid calculations
    if (!feeTier || feeTier <= 0 || !volumeUSD || volumeUSD <= 0 || !liquidity || liquidity <= 0) {
        return 0; // Return 0 APR if values are invalid or missing
    }

    const feePercentage = feeTier / 10000; // Convert feeTier to percentage (e.g., 3000 => 0.003)
    const dailyFeesCollected = volumeUSD * feePercentage; // Calculate daily fees generated by the pool
    const annualFees = dailyFeesCollected * 365; // Estimate annual fees assuming same daily volume

    // Avoid division by zero or very small liquidity values
    const apr = (annualFees / liquidity) * 100; // Calculate APR as a percentage

    return apr > 0 ? apr.toFixed(2) : 0; // Return APR with two decimal places
}
